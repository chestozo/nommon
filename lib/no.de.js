var no = require('./no.js');

//  ---------------------------------------------------------------------------------------------------------------  //

no.de = {};

//  ---------------------------------------------------------------------------------------------------------------  //

no.de.eval = function(js, namespace, sandbox) {
    if (namespace) {
        return Function( namespace, 'global', '"use strict"; return (' + js + ');' )(sandbox);
    } else {
        return Function( 'global', '"use strict"; return (' + js + ');' )();
    }
};

//  ---------------------------------------------------------------------------------------------------------------  //

module.exports = no.de;

